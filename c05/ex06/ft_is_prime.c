int	ft_is_prime(int nb)
{
	int n;

	n = 2;
	if (nb >= -1 && nb <= 1)
		return (0);
	if (nb < 0)
		nb = -nb;
	while (n < nb)
	{
		while (nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
