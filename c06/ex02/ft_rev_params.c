#include <unistd.h>

int	main(int argc, char *argv[])
{
	int n;

	n = argc - 1;
	while (n > 0)
	{
		while (*argv[n] != '\0')
		{
			write(1, argv[n], 1);
			argv[n]++;
		}
		write(1, "\n", 1);
		n--;
	}
}
