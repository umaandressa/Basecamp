#include <unistd.h>

void	ft_print_comb2(void)
{
	char a[5];

	a[0] = '/';
	a[2] = ' ';
	while ('9' > a[0]++)
	{
		a[1] = '0' - 1;
		while ('9' > a[1]++)
		{
			a[3] = '0' - 1;
			while ('9' > a[3]++)
			{
				a[4] = '0' - 1;
				while ('9' > a[4]++)
				{
					if (a[0] < a[3] || (a[0] == a[3] && a[1] < a[4]))
					{
						write(1, &a, 5);
						if (!(a[0] == '9' && a[1] == '8'))
							write(1, ", ", 2);
					}
				}
			}
		}
	}
}
